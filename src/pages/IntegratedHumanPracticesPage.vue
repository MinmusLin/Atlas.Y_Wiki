    <h3>4.1 Follow-up with 139 Initial Survey Respondents</h3>
    <p>After completing the project prototype design, we conducted follow-up interviews with the 139 individuals
      previously surveyed to gather their insights on the technology's strengths and weaknesses, helping us optimize the
      design in the iteration phase. </p>
    <img src='https://static.igem.wiki/teams/5503/accompanying-images/integratedhp23-1.webp' alt='IntegratedHP23'>
    <p>According to the feedback, the main strengths of the technology include efficient subcellular localization
      (84.17%, 117 people), as respondents believe that molecular tags can precisely localize proteins to specific
      subcellular regions in yeast, thereby improving localization efficiency. Another significant advantage is the
      increased product yield (64.75%, 90 people), where optimizing localization enhances enzyme efficiency, leading to
      higher production of target products. Respondents also highlighted the strong controllability of technology
      (64.75%, 90 people), noting that molecular tags allow for flexible regulation of enzyme activity and localization,
      offering greater control. Additionally, cost savings (54.68%, 76 people) were mentioned, as molecular tags can
      reduce resource waste caused by enzyme mislocalization, thus lowering production costs.</p>
    <img src='https://static.igem.wiki/teams/5503/accompanying-images/integratedhp24-1.webp'
         class='img-table'
         alt='IntegratedHP24'>
    <p>However, the challenges of the technology are equally notable. First, the complexity of selecting and combining
      molecular tags (70.5%, 98 people) may affect localization efficiency. Second, yeast stability issues (66.91%, 93
      people) pose a challenge, as subcellular localization may impact the overall growth and stability of the yeast.
      The side effects of the target products (62.59%, 87 people) are also a concern, as incorrect localization could
      result in harmful by-products, affecting overall outcomes. Lastly, the high technical difficulty (56.83%, 79
      people) involved in precise localization requires complex technical measures, making large-scale applications of
      the technology more challenging.</p>
